function integral = norm_quad(f, mu, sigma)

    xik = [
        0.0,
        0.565069583255575748526,
        1.13611558521092066632,
        1.71999257518648893242,
        2.32573248617385774545,
        2.96716692790560324849,
        3.66995037340445253473,
        4.49999070730939155366
    ];

    wk = [
        0.564100308726417532853,
        0.412028687498898627026,
        0.158488915795935746884,
        0.0307800338725460822287,
        0.00277806884291277589608,
        0.000100004441232499868127,
        0.00000105911554771106663578,
        0.00000000152247580425351702016
    ];

    integral = (dot(f(mu + sqrt(2)*sigma*xik), wk) + dot(f(mu - sqrt(2)*sigma*xik(2:end)), wk(2:end))) / sqrt(3.1415926535897932384626);
end